function readmeTemplate(projectName: string) {
  return `# ${projectName}

Next.js chatbot starter with a chat screen, an API route, and a small AI provider interface. It runs locally with a demo provider, so you can build the product flow before choosing a model vendor.

## Getting started

~~~bash
npm install
cp .env.example .env.local
npm run dev
~~~

Open http://localhost:3000/chat and send a message. The default provider returns a local demo response and does not call a paid service.

## Add a real AI provider

1. Install the SDK you want to use.
2. Add its environment variables to \`.env.local\`.
3. Replace \`demoAIProvider\` in \`src/lib/ai/provider.ts\` with a provider that implements \`AIProvider\`.
4. Keep provider-specific code inside \`src/lib/ai/provider.ts\` or a sibling module, then leave \`src/app/api/chat/route.ts\` focused on request validation and response shape.

The chat API expects an array of messages:

~~~json
{
  "messages": [
    { "role": "user", "content": "Draft a short welcome message." }
  ]
}
~~~

## Useful paths

- \`src/app/chat/page.tsx\` - chat route
- \`src/components/ChatPanel.tsx\` - client-side chat UI
- \`src/app/api/chat/route.ts\` - request validation and API response
- \`src/lib/ai/provider.ts\` - provider-neutral interface and demo provider
`;
}


function chatPanelTemplate() {
  return `"use client";

import { FormEvent, useMemo, useState } from "react";

type ChatRole = "user" | "assistant";

type ChatMessage = {
  id: string;
  role: ChatRole;
  content: string;
};

type ChatResponse = {
  message?: ChatMessage;
  error?: string;
};

const initialMessages: ChatMessage[] = [
  {
    id: "welcome",
    role: "assistant",
    content:
      "Ask a question and I will answer with the local demo provider until you wire a real model.",
  },
];

export function ChatPanel() {
  const [messages, setMessages] = useState<ChatMessage[]>(initialMessages);
  const [input, setInput] = useState("");
  const [error, setError] = useState<string | null>(null);
  const [isSending, setIsSending] = useState(false);

  const canSend = useMemo(
    () => input.trim().length > 0 && !isSending,
    [input, isSending],
  );

  async function handleSubmit(event: FormEvent<HTMLFormElement>) {
    event.preventDefault();

    const content = input.trim();

    if (!content || isSending) {
      return;
    }

    const userMessage: ChatMessage = {
      id: crypto.randomUUID(),
      role: "user",
      content,
    };
    const nextMessages = [...messages, userMessage];

    setMessages(nextMessages);
    setInput("");
    setError(null);
    setIsSending(true);

    try {
      const response = await fetch("/api/chat", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          messages: nextMessages.map(({ role, content }) => ({ role, content })),
        }),
      });
      const data = (await response.json()) as ChatResponse;

      if (!response.ok || !data.message) {
        throw new Error(data.error ?? "The chat API did not return a reply.");
      }

      setMessages((currentMessages) => [...currentMessages, data.message]);
    } catch (caughtError) {
      setError(
        caughtError instanceof Error
          ? caughtError.message
          : "The chat request failed.",
      );
    } finally {
      setIsSending(false);
    }
  }

  return (
    <div className="flex min-h-[620px] flex-col overflow-hidden rounded-lg border border-zinc-200 bg-white shadow-sm">
      <div className="flex-1 space-y-4 overflow-y-auto p-4 sm:p-6">
        {messages.map((message) => (
          <div
            key={message.id}
            className={
              message.role === "user"
                ? "ml-auto max-w-[80%] rounded-lg bg-zinc-950 px-4 py-3 text-sm leading-6 text-white"
                : "mr-auto max-w-[80%] rounded-lg bg-zinc-100 px-4 py-3 text-sm leading-6 text-zinc-800"
            }
          >
            {message.content}
          </div>
        ))}
      </div>

      {error ? (
        <p className="border-t border-red-100 bg-red-50 px-4 py-3 text-sm text-red-700">
          {error}
        </p>
      ) : null}

      <form
        onSubmit={handleSubmit}
        className="flex flex-col gap-3 border-t border-zinc-200 p-4 sm:flex-row"
      >
        <label className="sr-only" htmlFor="message">
          Message
        </label>
        <textarea
          id="message"
          value={input}
          onChange={(event) => setInput(event.target.value)}
          placeholder="Ask about onboarding, support, or your product workflow..."
          rows={2}
          className="min-h-12 flex-1 resize-none rounded-md border border-zinc-300 px-3 py-3 text-sm outline-none ring-zinc-950/10 placeholder:text-zinc-400 focus:ring-4"
        />
        <button
          type="submit"
          disabled={!canSend}
          className="rounded-md bg-zinc-950 px-5 py-3 text-sm font-medium text-white hover:bg-zinc-800 disabled:cursor-not-allowed disabled:bg-zinc-300"
        >
          {isSending ? "Sending" : "Send"}
        </button>
      </form>
    </div>
  );
}
`;
}

function chatRouteTemplate() {
  return `import { NextResponse } from "next/server";

import { getAIProvider, type ChatMessage } from "@/lib/ai/provider";

export const runtime = "nodejs";

type ChatRequestBody = {
  messages?: unknown;
};

export async function POST(request: Request) {
  let requestBody: unknown;

  try {
    requestBody = await request.json();
  } catch {
    return NextResponse.json(
      { error: "Send a valid JSON request body." },
      { status: 400 },
    );
  }

  const body: ChatRequestBody = isChatRequestBody(requestBody)
    ? requestBody
    : {};
  const messages = parseMessages(body.messages);

  if (messages.length === 0) {
    return NextResponse.json(
      { error: "Send at least one chat message." },
      { status: 400 },
    );
  }

  const provider = getAIProvider();
  const reply = await provider.generateReply({ messages });

  return NextResponse.json({
    message: {
      id: crypto.randomUUID(),
      role: "assistant",
      content: reply.content,
    },
    provider: provider.id,
  });
}

function isChatRequestBody(value: unknown): value is ChatRequestBody {
  return typeof value === "object" && value !== null;
}

function parseMessages(value: unknown): ChatMessage[] {
  if (!Array.isArray(value)) {
    return [];
  }

  return value.filter(isChatMessage).map((message) => ({
    role: message.role,
    content: message.content.trim(),
  }));
}

function isChatMessage(value: unknown): value is ChatMessage {
  if (typeof value !== "object" || value === null) {
    return false;
  }

  const candidate = value as Record<string, unknown>;

  return (
    (candidate.role === "user" ||
      candidate.role === "assistant" ||
      candidate.role === "system") &&
    typeof candidate.content === "string" &&
    candidate.content.trim().length > 0
  );
}
`;
}

function aiProviderTemplate() {
  return `export type ChatMessage = {
  role: "system" | "user" | "assistant";
  content: string;
};

export type GenerateReplyInput = {
  messages: ChatMessage[];
};

export type GenerateReplyResult = {
  content: string;
};

export type AIProvider = {
  id: string;
  generateReply: (input: GenerateReplyInput) => Promise<GenerateReplyResult>;
};

const demoAIProvider: AIProvider = {
  id: "demo",
  async generateReply(input) {
    const lastUserMessage = [...input.messages]
      .reverse()
      .find((message) => message.role === "user");

    if (!lastUserMessage) {
      return {
        content: "Send a user message and I will reply from the demo provider.",
      };
    }

    return {
      content: \`Demo reply: I received "\${lastUserMessage.content}". Replace demoAIProvider with your model SDK when you are ready.\`,
    };
  },
};

export function getAIProvider(): AIProvider {
  return demoAIProvider;
}
`;
}
